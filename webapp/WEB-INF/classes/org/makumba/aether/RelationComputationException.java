public class RelationComputationException extends Exception {

    public RelationComputationException() {
        super();
    }

    public RelationComputationException(String message, Throwable cause) {
        super(message, cause);
    }

    public RelationComputationException(String message) {
        super(message);
    }

    public RelationComputationException(Throwable cause) {
        super(cause);
    }

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

}
