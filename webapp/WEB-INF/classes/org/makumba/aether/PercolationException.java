package org.makumba.aether;

public class PercolationException extends Exception {

    public PercolationException() {
        super();
    }

    public PercolationException(String message, Throwable cause) {
        super(message, cause);
    }

    public PercolationException(String message) {
        super(message);
    }

    public PercolationException(Throwable cause) {
        super(cause);
    }

    private static final long serialVersionUID = 1L;

}
